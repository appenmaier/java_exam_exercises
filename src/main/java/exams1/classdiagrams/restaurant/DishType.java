package exams1.classdiagrams.restaurant;

/**
 * The category of a dish on a restaurant menu.
 *
 * @author Daniel Appenmaier
 * @version 1.0
 */
public enum DishType {
   /** A small starter served before the main course. */
   APPETIZER,
   /** The primary dish of a meal. */
   MAIN_COURSE,
   /** A sweet course served at the end of a meal. */
   DESSERT,
   /** A beverage. */
   DRINK
}
